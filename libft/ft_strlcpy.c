#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	t;

	t = 0;
	while (src[t] != '\0')
		t++;
	i = 0;
	if (dstsize == 0)
		return (t);
	while (src[i] != '\0' && i + 1 < dstsize)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (t);
}
